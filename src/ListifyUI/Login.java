    private void buttonLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonLoginMouseClicked

        DataArray da = new DataArray(10);
        if (Nama.equals(txtUsername.getText()) && Password.equals(txtPassword.getText())){
            //Menuju ke Menu home
            Home home = new Home(txtUsername.getText(),txtPassword.getText());
            home.setVisible(true);
            home.pack();
            home.setLocationRelativeTo(null);
      
            
            //menutup login dan Main
            Main main = new Main();
            main.dispose();
            this.dispose();
        }
        else if (da.findUsername(txtUsername.getText()) && da.findPassword(txtPassword.getText())) {
            //Menuju ke Menu home
            Home home = new Home(txtUsername.getText(),txtPassword.getText());
            home.setVisible(true);
            home.pack();
            home.setLocationRelativeTo(null);
      
            
            //menutup login dan Main
            Main main = new Main();
            main.dispose();
            this.dispose();

        } else if (txtPassword.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Password tidak boleh kosong !!");
            txtPassword.setText("");
            txtPassword.requestFocus();

        } else if (!da.findPassword(txtPassword.getText())) {
            System.out.println(da.findPassword(txtPassword.getText()));
            JOptionPane.showMessageDialog(null, "Password Salah !!");

            txtPassword.setText("");
            txtPassword.requestFocus();

        } else {
            JOptionPane.showMessageDialog(null, "Username atau Password Salah !!");
            txtUsername.setText("");
            txtPassword.setText("");
            txtUsername.requestFocus();
        }

    }//GEN-LAST:event_buttonLoginMouseClicked

